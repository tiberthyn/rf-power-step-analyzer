/* USER CODE BEGIN 4 */
void StartCapture(void)
{
    capture_done = 0;

    HAL_TIM_Base_Stop(&htim2);
    HAL_ADC_Stop_DMA(&hadc1);

    memset(adc_buffer, 0, sizeof(adc_buffer));

    HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adc_buffer, N_SAMPLES);
    HAL_TIM_Base_Start(&htim2);
}
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
    if (hadc->Instance == ADC1)
    {
        HAL_TIM_Base_Stop(&htim2);
        HAL_ADC_Stop_DMA(&hadc1);
        capture_done = 1;
    }
}
void SendBufferText(void)
{
    char msg[64];

    for (int i = 0; i < N_SAMPLES; i += 2)
    {
        uint16_t in  = adc_buffer[i];
        uint16_t out = adc_buffer[i + 1];

        int len = sprintf(msg, "%u,%u\r\n", in, out);

        HAL_UART_Transmit(&hlpuart1, (uint8_t*)msg, len, HAL_MAX_DELAY);
    }
}
void SendBufferBinary(void)
{
    uint8_t header[4] = {0xAA, 0x55, 0xAA, 0x55};

    HAL_UART_Transmit(&hlpuart1, header, sizeof(header), HAL_MAX_DELAY);
    HAL_UART_Transmit(&hlpuart1, (uint8_t*)&step_index, sizeof(step_index), HAL_MAX_DELAY);
    HAL_UART_Transmit(&hlpuart1, (uint8_t*)adc_buffer, sizeof(adc_buffer), HAL_MAX_DELAY);
}

